			<div class="TITLE_SCD">Vantagens.</div>
			<p>
				A principal vantagem para os produtores rurais é o tempo que 
				ganharão para fechar negociações de transporte para suas 
				cargas, já que as mesmas ficarão disponíveis para a consulta de 
				um número muito maior de transportadores. Sua 'lista de 
				contatos' se ampliará imensamente, e as chances deles perderem 
				vendas por falta de transportadores, ou entregar encomendas 
				fora do prazo diminuirá drasticamente. 
			</p>
			<p>
				Já os transportadores se beneficiarão circulando 
				menos tempo com seus caminhões vazios. Como citado 
				anteriormente, eles poderão consultar as cargas lançadas no 
				sistema e fechar negociações com os produtores de acordo com 
				seus interesses/necessidades. Diminuindo o número de 
				quilômetros rodados com o caminhão vazio, diminuirão também os 
				gastos de combustível e pedágio, pois estarão recebendo pelo 
				transporte das cargas. 
			</p>
			<p class="LAST">
				Além disso, para cada negociação fechada, o produtor rural 
				responsável poderá avaliar aspectos do transportador com o qual 
				fechou a negociação. Isto incentivará os transportadores a 
				prestarem bons serviços e cumprirem os prazos, para ficarem bem 
				avaliados no sistema, já que essas avaliações poderão ser 
				visualizadas por outros produtores futuramente, conforme o 
				transportador avaliado vá se interessando por novas cargas 
				lançadas no sistema. Essa avaliação servirá como um parâmetros 
				para auxiliar os produtores rurais na escolha de 
				transportadores que se interessarem em suas cargas.  
			</p>
