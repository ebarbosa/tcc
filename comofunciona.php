			<div class="TITLE_SCD">Problema encontrado.</div>
			<p>
				Atualmente, o número de produtores rurais e transportadores que 
				trabalham de forma autônoma no país é muito expressivo. 
				Entretanto, a comunicação entre estas partes não acontece de 
				uma forma muito eficaz.
			</p>
			<p>
				Essa comunicação se reestringe, basicamente, a uma 'lista de 
				contatos' dos produtores rurais, que entram em contato com os 
				transportadores	conforme suas necessidades. Existem ainda 
				agentes instalados em postos rodoviários com grande 
				concentração de transportadores. Estes agentes, por sua vez, 
				possuem uma 'lista de contatos' muito maior, só que de 
				produtores rurais. Mantendo contato com os produtores de suas 
				listas, os agentes tem conhecimento de cargas disponíveis e 
				repassam essas informações aos transportadores que passam pelos 
				postos nos quais os mesmos estão instalados.
			</p>
			<p class="LAST">
				É desta forma que na maioria das vezes as negociações de 
				transporte acontecem. Nós, da SATC, notamos esta situação e 
				desenvolvemos um sistema que visa facilitar a comunicação entre 
				transportadores e produtores rurais, aumentando o número de 
				negociações entre as partes.
			</p>
			<div class="BACK"><a href="index.php">Voltar para a página inicial.</a></div>
			<div class="TITLE_SCD">Nosso objetivo.</div>
			<p>
				A intenção do sistema é essencialmente aperfeiçoar a comuniação 
				entre produtores rurais e transportadores, permitindo que os 
				produtores lançem no sistema suas cargas que necessitam de 
				transporte, e que essas fiquem disponíves para a consulta de 
				transportadores. Uma vez que isto acontece, transportadores 
				poderão entrar em contato com os produtores responsáveis pelas 
				cargas que sejam de seu interesse, de acordo com suas 
				localizações.
			</p>
			<p>
				Um produtor pode lançar no sistema que terá uma carga 
				disponível para transporte até certo lugar, e o carregamento 
				deverá ser feito a partir de determinada data. Com estas 
				informações à disposição para consulta dos transportadores, 
				estes poderão ver as cargas que sejam possíveis deles fazerem o 
				transporte, de acordo com a data e os locais das mesmas. 
			</p>
			<p class="LAST">
				Por exemplo, imagine que um transportador tenha feito uma 
				entrega no estado da Bahia, e esteja retornando para sua casa, 
				no estado do Rio Grande do Sul. Este seria um grande percurso 
				para percorrer com o caminhão vazio, e ele provavelmente 
				procuraria alguma carga para transportar neste caminho de volta 
				à sua casa. E é exatamente esta a intenção do sistema, com um 
				grande número de cargas lançadas, este transportador terá 
				chances muito maiores de encontrar uma carga para ser 
				transportada que se adeque ao seu percurso e a sua data de 
				viagem. Ele poderá achar, por exemplo, uma carga para ser 
				transportada de Minas Gerais até Santa Catarina, e que bata com 
				a data em que ele fará a viagem. Nesta situação, ele faria 
				quase todo seu percurso (que ele faria de qualquer maneira) com 
				o caminhão carregado, ao invés de percorrer o mesmo caminho com 
				o caminhão vazio. 
			</p>
